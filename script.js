const whatsappNumber = "263788278675";
const productGrid = document.getElementById("productGrid");
const featuredGrid = document.getElementById("featuredGrid");
const filtersEl = document.getElementById("filters");
const searchInput = document.getElementById("searchInput");
const yearEl = document.getElementById("year");

let activeSeries = "All";

const money = (value) => `$${value}`;
const startingPrice = (product) => Math.min(...product.options.map(option => option.price));
const cleanSeries = (series) => series.replace(" Series", "");

function orderLink(product, option) {
  const message = `Hi ShaneMobilez, I want to order the ${product.model} ${option.storage} for ${money(option.price)}. Is it available?`;
  return `https://wa.me/${whatsappNumber}?text=${encodeURIComponent(message)}`;
}

function productCard(product, isFeatured = false) {
  const options = product.options
    .map(option => `
      <div class="option">
        <b>${option.storage}</b>
        <strong>${money(option.price)}</strong>
        <a class="order-small" href="${orderLink(product, option)}" target="_blank" rel="noopener">Order</a>
      </div>
    `)
    .join("");

  return `
    <article class="card">
      <div class="media">
        <img src="${product.image}" alt="${product.model}" loading="lazy">
      </div>
      <div class="content">
        <span class="tag">${product.series}</span>
        <h3>${product.model}</h3>
        <p class="desc">${product.condition}</p>
        <div class="starting">
          <span>Starting from</span>
          <strong>${money(startingPrice(product))}</strong>
        </div>
        <div class="options">${options}</div>
      </div>
    </article>
  `;
}

function renderFilters() {
  const series = ["All", ...new Set(products.map(product => cleanSeries(product.series)))];
  filtersEl.innerHTML = series
    .map(seriesName => `<button class="filter ${seriesName === activeSeries ? "active" : ""}" data-series="${seriesName}">${seriesName}</button>`)
    .join("");

  document.querySelectorAll(".filter").forEach(button => {
    button.addEventListener("click", () => {
      activeSeries = button.dataset.series;
      renderProducts();
      renderFilters();
    });
  });
}

function renderProducts() {
  const query = searchInput.value.trim().toLowerCase();

  const filtered = products.filter(product => {
    const matchesSeries = activeSeries === "All" || cleanSeries(product.series) === activeSeries;
    const matchesSearch = product.model.toLowerCase().includes(query) || product.series.toLowerCase().includes(query);
    return matchesSeries && matchesSearch;
  });

  productGrid.innerHTML = filtered.length
    ? filtered.map(product => productCard(product)).join("")
    : `<div class="empty">No iPhone matched your search.</div>`;
}

function renderFeatured() {
  const featuredModels = [
    "iPhone 16 Pro Max",
    "iPhone 15 Pro Max",
    "iPhone 14 Pro Max",
    "iPhone 13 Pro Max"
  ];
  const featured = featuredModels
    .map(name => products.find(product => product.model === name))
    .filter(Boolean);

  featuredGrid.innerHTML = featured.map(product => productCard(product, true)).join("");
}

searchInput.addEventListener("input", renderProducts);
yearEl.textContent = new Date().getFullYear();

renderFeatured();
renderFilters();
renderProducts();
